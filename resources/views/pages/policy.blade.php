        khususnya dalam menangani pengaduan masyarakat,
        no wrong door policy merupakan implementasi dari Pasal 36 Ayat (1), (2) dan (3) UU Pelayanan Publik,
        bahwa Penyelenggara pelayanan publik (penyelenggara) berkewajiban menyediakan sarana pengaduan dan menugaskan
        pelaksana yang kompeten dalam pengelolaan pengaduan. Selain itu, penyelenggara berkewajiban mengelola pengaduan
        yang berasal dari penerima pelayanan, rekomendasi Ombudsman, DPR, DPRD Provinsi/Kabupaten/Kota.
        Penyelenggara juga mempunyai kewajiban untuk menindaklanjuti hasil pengelolaan pengaduan, serta penyelenggara
        berkewajiban mengumumkan nama dan alamat penanggung jawab pengelola pengaduan serta sarana pengaduan yang
        disediakan.
        Hal ini semata-mata untuk menjamin dan emmberikan kepastian bahwa pengaduan masyarakat akan diterima oleh
        petugas khusus,
        dan segera ditindaklanjuti sesuai dengan mekanisme yang berlaku pada sutu instansi penyelenggara.
